
public class Calculo {
	public static float ExecutaSoma (float valor1, float valor2) {
		float Soma = valor1 + valor2;
		return Soma;
	}
	
	public static float ExecutaSubtracao (float valor1, float valor2) {
		float Subtracao = valor1 - valor2;
		return Subtracao;
	}
	
	public static float ExecutaProduto (float valor1, float valor2) {
		float produto = valor1 * valor2;
		return produto;
	}
	
	
	
	
}
